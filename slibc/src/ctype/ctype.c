#include <ctype.h>

#define CNTRL_MASK  (0x0001)
#define PRINT_MASK  (0x0002)
#define SPACE_MASK  (0x0004)
#define BLANK_MASK  (0x0008)
#define GRAPH_MASK  (0x0010)
#define PUNCT_MASK  (0x0020)
#define ALNUM_MASK  (0x0040)
#define ALPHA_MASK  (0x0080)
#define UPPER_MASK  (0x0100)
#define LOWER_MASK  (0x0200)
#define DIGIT_MASK  (0x0400)
#define XDIGIT_MASK (0x0800)

static int ctypes[128] = {
    0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, /* 000 - 007 */
    0x0001, 0x000d, 0x0005, 0x0005, 0x0005, 0x0005, 0x0001, 0x0001, /* 008 - 015 */
    0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, /* 016 - 023 */
    0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, 0x0001, /* 024 - 031 */
    0x000e, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, /* 032 - 039 */
    0x0032, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, /* 040 - 047 */
    0x0c52, 0x0c52, 0x0c52, 0x0c52, 0x0c52, 0x0c52, 0x0c52, 0x0c52, /* 048 - 055 */
    0x0c52, 0x0c52, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, /* 056 - 063 */
    0x0032, 0x09d2, 0x09d2, 0x09d2, 0x09d2, 0x09d2, 0x09d2, 0x01d2, /* 064 - 071 */
    0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, /* 072 - 079 */
    0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, 0x01d2, /* 080 - 087 */
    0x01d2, 0x01d2, 0x01d2, 0x0032, 0x0032, 0x0032, 0x0032, 0x0032, /* 088 - 095 */
    0x0032, 0x0ad2, 0x0ad2, 0x0ad2, 0x0ad2, 0x0ad2, 0x0ad2, 0x02d2, /* 096 - 103 */
    0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, /* 104 - 111 */
    0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, 0x02d2, /* 112 - 119 */
    0x02d2, 0x02d2, 0x02d2, 0x0032, 0x0032, 0x0032, 0x0032, 0x0001  /* 120 - 127 */
};

int isalnum(int c) {
    return ctypes[c] & ALNUM_MASK;
}

int isalpha(int c) {
    return ctypes[c] & ALPHA_MASK;
}

int isblank(int c) {
    return ctypes[c] & BLANK_MASK;
}

int islower(int c) {
    return ctypes[c] & LOWER_MASK;
}

int isupper(int c) {
    return ctypes[c] & UPPER_MASK;
}

int isdigit(int c) {
    return ctypes[c] & DIGIT_MASK;
}

int isxdigit(int c) {
    return ctypes[c] & XDIGIT_MASK;
}

int iscntrl(int c) {
    return ctypes[c] & CNTRL_MASK;
}

int isgraph(int c) {
    return ctypes[c] & GRAPH_MASK;
}

int isspace(int c) {
    return ctypes[c] & SPACE_MASK;
}

int isprint(int c) {
    return ctypes[c] & PRINT_MASK;
}

int ispunct(int c) {
    return ctypes[c] & PUNCT_MASK;
}

int toupper(int c) {
    return isupper(c) ? c : c - 32;
}

int tolower(int c) {
    return islower(c) ? c : c + 32;
}
